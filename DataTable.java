    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        double BMI, BMR, recIntake;
        String categories;
        DefaultTableModel model = (DefaultTableModel) recTable.getModel();
        
         try{
            pst = conn.prepareStatement("UPDATE userdata SET ID = ?, FirstName = ?, LastName = ?, UserEmail = ?, UserAge = ?, UserGrade = ?, UserGender = ?, "
                    + "UserHeight = ?, UserWeight = ?, UserBMI = ?, Categories = ?, UserBMR = ?, ActivityLevels = ?, "
                    + "RecCalories = ?, UserSymptoms = ? WHERE ID = ?");
            pst.setString(1, model.getValueAt(recTable.getSelectedRow(),0).toString());
            pst.setString(2, txtFirstName.getText());
            pst.setString(3, txtLastName.getText());
            pst.setString(4, txtEmail.getText());
            pst.setString(5, txtAge.getText());
            pst.setString(6, txtGrade.getText());
            pst.setString(7, boxGender.getSelectedItem().toString());
            pst.setString(8, txtHeight.getText());
            pst.setString(9, txtWeight.getText());
            pst.setString(13, boxActivity.getSelectedItem().toString());
            pst.setString(15, txtConditions.getText());
            
            MedicalRecords calculator = new MedicalRecords();
            BMI = calculator.calcBMI(Double.parseDouble(txtWeight.getText()),Double.parseDouble(txtHeight.getText()));
        
            categories = calculator.categoriesBMI(BMI);
            
            BMR = calculator.calcBMR((String) boxGender.getSelectedItem(), Double.parseDouble(txtWeight.getText()), Double.parseDouble(txtHeight.getText()), Integer.parseInt(txtAge.getText()));
            recIntake = calculator.calcRecIntake(BMR, boxActivity.getSelectedIndex());
            
            pst.setDouble(10, BMI);
            pst.setString(11, categories);
            pst.setDouble(12, BMR);
            pst.setDouble(14, recIntake);
            pst.setString(16, model.getValueAt(recTable.getSelectedRow(),0).toString());
            pst.executeUpdate(); 
            model.setRowCount(0);
            showStudent();
            JOptionPane.showMessageDialog(null, "Data Updated");
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_updateButtonActionPerformed


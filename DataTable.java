    private void recTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_recTableMouseClicked
        // TODO add your handling code here:
        int i = recTable.getSelectedRow();
        TableModel model = recTable.getModel();
        DecimalFormat df = new DecimalFormat("#.00"); //Formats the decimals to 0 decimal places
        
        txtFirstName.setText(model.getValueAt(i,1).toString());
        txtLastName.setText(model.getValueAt(i,2).toString());
        txtEmail.setText(model.getValueAt(i,3).toString());
        txtAge.setText(model.getValueAt(i,4).toString());
        txtGrade.setText(model.getValueAt(i,5).toString());
        String sGender = model.getValueAt(i,6).toString();
        switch(sGender)
        {
            case "Male":
            boxGender.setSelectedIndex(0);
            break;
            
            case "Female":
            boxGender.setSelectedIndex(1);
            break;
        }
       
        txtHeight.setText(model.getValueAt(i,7).toString());
        txtWeight.setText(model.getValueAt(i,8).toString());
        txtBMI.setText(df.format(model.getValueAt(i,9)));
        txtCategories.setText(model.getValueAt(i,10).toString());
        txtBMR.setText(df.format(model.getValueAt(i,11)));
        String sActivity = model.getValueAt(i,12).toString();
        switch(sActivity)
        {
            case "Little to no exercise":
            boxActivity.setSelectedIndex(0);
            break;
            
            case "Light exercise ":
            boxActivity.setSelectedIndex(1);
            break;
            
            case "Moderate exercise ":  
            boxActivity.setSelectedIndex(2);
            break;
            
            case "Heavy exercise ":
            boxActivity.setSelectedIndex(3);
            break;
            
            case "Very heavy exercise ":
            boxActivity.setSelectedIndex(4);
            break;
        }
        txtRecIntake.setText(df.format(model.getValueAt(i,13)));
        txtConditions.setText(model.getValueAt(i,14).toString());
    }//GEN-LAST:event_recTableMouseClicked


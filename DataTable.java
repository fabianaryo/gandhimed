    public ArrayList<Data> StudentList()
    {
        ArrayList<Data> StudentList = new ArrayList<>();
        try
        {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/useraccount?zeroDateTimeBehavior=convertToNull","root", "");
            st = conn.createStatement();
            rs = st.executeQuery("SELECT * FROM userdata");
            Data pupil;
            while (rs.next())
            {
                pupil = new Data(rs.getInt("ID"), rs.getString("FirstName"), rs.getString("LastName"), rs.getString("UserEmail"), rs.getInt("UserAge"), 
                        rs.getString("UserGrade"), rs.getString("UserGender"), rs.getInt("UserHeight"), rs.getInt("UserWeight"), rs.getDouble("UserBMI"), rs.getString("Categories"),
                        rs.getDouble("UserBMR"), rs.getString("ActivityLevels"), rs.getDouble("RecCalories"), rs.getString("UserSymptoms"));
                StudentList.add(pupil);
            }
        } 
        
        catch (SQLException ex) {
        Logger.getLogger(DataTable.class.getName()).log(Level.SEVERE, null, ex);
    }
    return StudentList;
    }
    
    public ArrayList<Data> SearchedStudent(String keyword)
    {
        ArrayList<Data> searchStudent = new ArrayList<Data>();
        
        try{
            st = conn.createStatement();
            rs = st.executeQuery("SELECT * FROM userdata WHERE CONCAT(`ID`, `FirstName`, `LastName`, `UserEmail`, `UserAge`, `UserGrade`, `UserGender`, "
                    + "`UserHeight`, `UserWeight`, `UserBMI`, `Categories`, `UserBMR`, `ActivityLevels`, `UserSymptoms`) LIKE '%"+keyword+"%'");
            Data searched;
            while (rs.next())
            {
                searched = new Data(rs.getInt("ID") , rs.getString("FirstName"), rs.getString("LastName"), rs.getString("UserEmail"), 
                        rs.getInt("UserAge"), rs.getString("UserGrade"), rs.getString("UserGender"), rs.getInt("UserHeight"), 
                                 rs.getInt("UserWeight"), rs.getDouble("UserBMI"), rs.getString("Categories"), 
                        rs.getDouble("UserBMR"), rs.getString("ActivityLevels"), rs.getDouble("RecCalories"), rs.getString("UserSymptoms"));
                searchStudent.add(searched);
            }
            
        }catch(Exception ex){
            Logger.getLogger(DataTable.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return searchStudent;
    }
    
